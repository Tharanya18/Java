package Day1;

public class Exp6 {
   public static void main(String[] args)
   {
	   //short to int
	   short s = 126;
	   int num = s; 
	   System.out.println(num);
	   
	   //int to long
	   int n = 30;
	   long l = n;
	   System.out.println(l);
	   
	   //float to double
	   float fl = 0.4358756f;
	   double d = fl;
	   System.out.println(d);
   }
}
